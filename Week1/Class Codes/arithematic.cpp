#include <iostream>

using namespace std;

int main(){
    // + , - , * , / , %
    cout << 10 + 2 << endl;  // 12
    cout << 10 - 2 << endl;  // 8
    cout << 10 * 2 << endl;  // 20   
    cout << 10 / 2 << endl;  // 5
    cout << 10 % 2 << endl;  // 0

    cout << 10.0 / 3 << endl; // 3.3333
    return 0;
}

// / -> the division operator gives quotient as answer!!
// % -> the modulo operator gives remainder as answer!!


// If we divide  2 integers, the result is always an integer.
// if we want answer in floating values we need to make atleast one either the dividend or the divisor a floating values!!